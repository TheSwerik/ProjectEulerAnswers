# Personal life Eyesight deterioration Euler's eyesight worsened throughout his mathematical career. In 1738,
# three years after nearly expiring from fever, he became almost blind in his right eye, but Euler rather blamed the
# painstaking work on cartography he performed for the St. Petersburg Academy for his condition. Euler's vision in
# that eye worsened throughout his stay in Germany, to the extent that Frederick referred to him as "Cyclops". Euler
# remarked on his loss of vision, "Now I will have fewer distractions."[24] He later developed a cataract in his left
# eye, which was discovered in 1766. Just a few weeks after its discovery, a failed surgical restoration rendered him
# almost totally blind. He was 59 years old then. However, his condition appeared to have little effect on his
# productivity, as he compensated for it with his mental calculation skills and exceptional memory. For example,
# Euler could repeat the Aeneid of Virgil from beginning to end without hesitation, and for every page in the edition
# he could indicate which line was the first and which the last. With the aid of his scribes, Euler's productivity on
# many areas of study actually increased. He produced, on average, one mathematical paper every week in the year
# 1775.[25] The Eulers bore a double name, Euler-Schölpi, the latter of which derives from schelb and schief,
# signifying squint-eyed, cross-eyed, or crooked. This suggests that the Eulers may have had a susceptibility to eye
# problems.[26]
#
# Return to Russia and death In 1760, with the Seven Years' War raging, Euler's farm in Charlottenburg was ransacked
# by advancing Russian troops. Upon learning of this event, General Ivan Petrovich Saltykov paid compensation for the
# damage caused to Euler's estate, with Empress Elizabeth of Russia later adding a further payment of 4000 roubles—an
# exorbitant amount at the time.[27] The political situation in Russia stabilized after Catherine the Great's
# accession to the throne, so in 1766 Euler accepted an invitation to return to the St. Petersburg Academy. His
# conditions were quite exorbitant—a 3000 ruble annual salary, a pension for his wife, and the promise of
# high-ranking appointments for his sons. All of these requests were granted. He spent the rest of his life in
# Russia. However, his second stay in the country was marred by tragedy. A fire in St. Petersburg in 1771 cost him
# his home, and almost his life. In 1773, he lost his wife Katharina after 40 years of marriage.
#
# Three years after his wife's death, Euler married her half-sister, Salome Abigail Gsell (1723–1794).[28] This
# marriage lasted until his death. In 1782 he was elected a Foreign Honorary Member of the American Academy of Arts
# and Sciences.[29]
#
# In St. Petersburg on 18 September 1783, after a lunch with his family, Euler was discussing the newly discovered
# planet Uranus and its orbit with a fellow academician Anders Johan Lexell, when he collapsed from a brain
# hemorrhage. He died a few hours later.[30] Jacob von Staehlin-Storcksburg wrote a short obituary for the Russian
# Academy of Sciences and Russian mathematician Nicolas Fuss, one of Euler's disciples, wrote a more detailed eulogy,
# [31] which he delivered at a memorial meeting. In his eulogy for the French Academy, French mathematician and
# philosopher Marquis de Condorcet, wrote:
#
# il cessa de calculer et de vivre— ... he ceased to calculate and to live.[32]
#
#
# Euler's grave at the Alexander Nevsky Monastery Euler was buried next to Katharina at the Smolensk Lutheran
# Cemetery on Goloday Island. In 1785, the Russian Academy of Sciences put a marble bust of Leonhard Euler on a
# pedestal next to the Director's seat and, in 1837, placed a headstone on Euler's grave. To commemorate the 250th
# anniversary of Euler's birth, the headstone was moved in 1956, together with his remains, to the 18th-century
# necropolis at the Alexander Nevsky Monastery.
