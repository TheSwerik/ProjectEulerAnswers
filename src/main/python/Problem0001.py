# Leonhard Euler (/ˈɔɪlər/ OY-lər;[2] German: [ˈɔʏlɐ] (About this soundlisten)[3]; 15 April 1707 – 18 September 1783)
# was a Swiss mathematician, physicist, astronomer, geographer, logician and engineer who made important and
# influential discoveries in many branches of mathematics, such as infinitesimal calculus and graph theory,
# while also making pioneering contributions to several branches such as topology and analytic number theory. He also
# introduced much of the modern mathematical terminology and notation, particularly for mathematical analysis,
# such as the notion of a mathematical function.[4] He is also known for his work in mechanics, fluid dynamics,
# optics, astronomy and music theory.[5] Euler was one of the most eminent mathematicians of the 18th century and is
# held to be one of the greatest in history. He is also widely considered to be the most prolific, as his collected
# works fill 92 volumes,[6] more than anyone else in the field. He spent most of his adult life in Saint Petersburg,
# Russia, and in Berlin, then the capital of Prussia. A statement attributed to Pierre-Simon Laplace expresses
# Euler's influence on mathematics: "Read Euler, read Euler, he is the master of us all." Early life Leonhard Euler
# was born on 15 April 1707, in Basel, Switzerland, to Paul III Euler, a pastor of the Reformed Church,
# and Marguerite née Brucker, a pastor's daughter. He had two younger sisters, Anna Maria and Maria Magdalena,
# and a younger brother, Johann Heinrich.[9] Soon after the birth of Leonhard, the Eulers moved from Basel to the
# town of Riehen, where Leonhard spent most of his childhood. Paul was a friend of the Bernoulli family; Johann
# Bernoulli, then regarded as Europe's foremost mathematician, would eventually be the most important influence on
# young Leonhard.
#
# Euler's formal education started in Basel, where he was sent to live with his maternal grandmother. In 1720,
# at age thirteen, he enrolled at the University of Basel. In 1723, he received a Master of Philosophy with a
# dissertation that compared the philosophies of Descartes and Newton. During that time, he was receiving Saturday
# afternoon lessons from Johann Bernoulli, who quickly discovered his new pupil's incredible talent for mathematics.[
# 10] At that time Euler's main studies included theology, Greek and Hebrew at his father's urging to become a
# pastor, but Bernoulli convinced his father that Leonhard was destined to become a great mathematician.
#
# In 1726, Euler completed a dissertation on the propagation of sound with the title De Sono.[11] At that time,
# he was unsuccessfully attempting to obtain a position at the University of Basel. In 1727, he first entered the
# Paris Academy Prize Problem competition; the problem that year was to find the best way to place the masts on a
# ship. Pierre Bouguer, who became known as "the father of naval architecture", won and Euler took second place.
# Euler later won this annual prize twelve times.[12]
#
# Career Saint Petersburg Around this time Johann Bernoulli's two sons, Daniel and Nicolaus, were working at the
# Imperial Russian Academy of Sciences in Saint Petersburg. On 31 July 1726, Nicolaus died of appendicitis after
# spending less than a year in Russia.[13][14] When Daniel assumed his brother's position in the mathematics/physics
# division, he recommended that the post in physiology that he had vacated be filled by his friend Euler. In November
# 1726 Euler eagerly accepted the offer, but delayed making the trip to Saint Petersburg while he unsuccessfully
# applied for a physics professorship at the University of Basel.[15]
#
#
# 1957 Soviet Union stamp commemorating the 250th birthday of Euler. The text says: 250 years from the birth of the
# great mathematician, academician Leonhard Euler. Euler arrived in Saint Petersburg on 17 May 1727. He was promoted
# from his junior post in the medical department of the academy to a position in the mathematics department. He
# lodged with Daniel Bernoulli with whom he often worked in close collaboration. Euler mastered Russian and settled
# into life in Saint Petersburg. He also took on an additional job as a medic in the Russian Navy.[16]
#
# The Academy at Saint Petersburg, established by Peter the Great, was intended to improve education in Russia and to
# close the scientific gap with Western Europe. As a result, it was made especially attractive to foreign scholars
# like Euler. The academy possessed ample financial resources and a comprehensive library drawn from the private
# libraries of Peter himself and of the nobility. Very few students were enrolled in the academy to lessen the
# faculty's teaching burden. The academy emphasized research and offered to its faculty both the time and the freedom
# to pursue scientific questions.[12]
#
# The Academy's benefactress, Catherine I, who had continued the progressive policies of her late husband,
# died on the day of Euler's arrival. The Russian nobility then gained power upon the ascension of the
# twelve-year-old Peter II. The nobility, suspicious of the academy's foreign scientists, cut funding and caused
# other difficulties for Euler and his colleagues.
#
# Conditions improved slightly after the death of Peter II, and Euler swiftly rose through the ranks in the academy
# and was made a professor of physics in 1731. Two years later, Daniel Bernoulli, who was fed up with the censorship
# and hostility he faced at Saint Petersburg, left for Basel. Euler succeeded him as the head of the mathematics
# department.[17]
#
# On 7 January 1734, he married Katharina Gsell (1707–1773), a daughter of Georg Gsell, a painter from the Academy
# Gymnasium.[18] The young couple bought a house by the Neva River. Of their thirteen children, only five survived
# childhood.[19]
