# Mathematical notation Euler introduced and popularized several notational conventions through his numerous and
# widely circulated textbooks. Most notably, he introduced the concept of a function[4] and was the first to write f(
# x) to denote the function f applied to the argument x. He also introduced the modern notation for the trigonometric
# functions, the letter e for the base of the natural logarithm (now also known as Euler's number), the Greek letter
# Σ for summations and the letter i to denote the imaginary unit.[34] The use of the Greek letter π to denote the
# ratio of a circle's circumference to its diameter was also popularized by Euler, although it originated with Welsh
# mathematician William Jones.[35]
